/**
 * Find a single algorithm by its slug across all categories.
 * @param {string} slug - The URL-friendly identifier for the algorithm.
 * @returns {object|null} The algorithm object or null if not found.
 */
export function getAlgorithm(slug) {
  for (const algos of Object.values(algorithmsByCategory)) {
    const found = algos.find(a => a.slug === slug);
    if (found) return found;
  }
  return null;
}

/**
 * Get all algorithms belonging to a specific category.
 * @param {string} categorySlug - The category identifier.
 * @returns {Array} Array of algorithm objects for the category.
 */
export function getAlgorithmsByCategory(categorySlug) {
  return algorithmsByCategory[categorySlug] || [];
}

/**
 * Get a flat array of all algorithms across every category.
 * @returns {Array} All algorithm objects.
 */
export function getAllAlgorithms() {
  return Object.values(algorithmsByCategory).flat();
}

/**
 * Get only the algorithms that have been implemented.
 * @returns {Array} Algorithm objects where implemented is true.
 */
export function getImplementedAlgorithms() {
  return getAllAlgorithms().filter(a => a.implemented);
}
